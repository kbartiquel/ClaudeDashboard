  /** Get saved profile ID or default */
  getActiveProfileId() {
    return localStorage.getItem('terminalProfile') || 'claude-dark';
  },

  /** Get the active profile object */
  getActiveProfile() {
    const id = this.getActiveProfileId();
    return this.profiles[id] || this.profiles['claude-dark'];
  },

  /** Save selected profile */
  setActiveProfile(profileId) {
    if (this.profiles[profileId]) {
      localStorage.setItem('terminalProfile', profileId);
    }
  },

  /** Apply profile to an existing xterm instance */
  applyProfile(termInstance, profileId) {
    const profile = this.profiles[profileId] || this.profiles['claude-dark'];
    termInstance.options.theme = profile.theme;
    termInstance.options.fontSize = profile.fontSize;
    termInstance.options.fontFamily = profile.fontFamily;
  },

  /** Get list of all profiles for UI */
  list() {
    return Object.entries(this.profiles).map(([id, p]) => ({
      id,
      name: p.name,
      bgColor: p.theme.background,
      fgColor: p.theme.foreground,
    }));
  },
